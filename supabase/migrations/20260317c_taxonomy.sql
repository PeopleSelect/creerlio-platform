-- ============================================================
-- Global 3-Layer Industry & Profession Taxonomy
-- Layer 1: Industries  |  Layer 2: Sectors  |  Layer 3: Professions
-- Supports 2000+ professions across 29 industries
-- ============================================================

CREATE TABLE IF NOT EXISTS public.industries (
  id          serial      PRIMARY KEY,
  name        text        NOT NULL UNIQUE,
  description text
);

CREATE TABLE IF NOT EXISTS public.industry_sectors (
  id          serial  PRIMARY KEY,
  industry_id integer NOT NULL REFERENCES public.industries(id) ON DELETE CASCADE,
  name        text    NOT NULL,
  UNIQUE (industry_id, name)
);
CREATE INDEX IF NOT EXISTS idx_sectors_industry ON public.industry_sectors(industry_id);

CREATE TABLE IF NOT EXISTS public.sector_professions (
  id        serial  PRIMARY KEY,
  sector_id integer NOT NULL REFERENCES public.industry_sectors(id) ON DELETE CASCADE,
  name      text    NOT NULL,
  UNIQUE (sector_id, name)
);
CREATE INDEX IF NOT EXISTS idx_professions_sector ON public.sector_professions(sector_id);
CREATE INDEX IF NOT EXISTS idx_professions_name   ON public.sector_professions USING gin(to_tsvector('english', name));

-- RLS: public read-only; only service role may write
ALTER TABLE public.industries        ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.industry_sectors  ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.sector_professions ENABLE ROW LEVEL SECURITY;

DO $$ BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_policies WHERE tablename='industries' AND policyname='public_read') THEN
    CREATE POLICY public_read ON public.industries        FOR SELECT TO anon, authenticated USING (true);
  END IF;
  IF NOT EXISTS (SELECT 1 FROM pg_policies WHERE tablename='industry_sectors' AND policyname='public_read') THEN
    CREATE POLICY public_read ON public.industry_sectors  FOR SELECT TO anon, authenticated USING (true);
  END IF;
  IF NOT EXISTS (SELECT 1 FROM pg_policies WHERE tablename='sector_professions' AND policyname='public_read') THEN
    CREATE POLICY public_read ON public.sector_professions FOR SELECT TO anon, authenticated USING (true);
  END IF;
END $$;

GRANT SELECT ON public.industries        TO anon, authenticated;
GRANT SELECT ON public.industry_sectors  TO anon, authenticated;
GRANT SELECT ON public.sector_professions TO anon, authenticated;

